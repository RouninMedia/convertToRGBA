  if (in_array($input, $color_Keywords)) {
    
    $input_Types['hex'] = FALSE;
  }
    
  else {
  
    $input_Types['colorKeyword'] = FALSE;
    
    if (substr($input, 0, 1) === '#') {

      $input = substr($input, 1);
    }

    if (preg_match('/[g-z]/', $input)) {

      $input_Types['hex'] = FALSE;
    }

    if ((strlen($input) !== 3) && (strlen($input) !== 4) && (strlen($input) !== 6) && (strlen($input) !== 8)) {

      $input_Types['hex'] = FALSE;
    }
  }

  return $input_Types;
}

